#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	srclen;
	size_t	destlen;

	i = 0;
	if (!dest || !src)
		return (0);
	destlen = ft_strlen(dest);
	srclen = ft_strlen(src);
	if (!size)
		return (srclen);
	if (destlen >= size)
		return (size + srclen);
	while ((size - 1 > destlen + i) && src[i])
	{
		dest[destlen + i] = src[i];
		i++;
	}
	dest[destlen + i] = '\0';
	return (destlen + srclen);
}
